package day6.whileEx;

public class SumEx {

	public static void main(String[] args) {
		/*
		 * 1부터 n까지 합을 구하는 코드를 작성하세요.(n은 10)
		 * 반복횟수 : 
		 * 규칙성 :
		 * 반복문 종료 후 :
		 * 
		 */
		
		int n = 10;
		int i = 0;
		int sum = 0;
		while(n >= i) {
			sum += i;
			i++;
		}
		
		System.out.println(sum);
		
	}

}
