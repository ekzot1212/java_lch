package day6.forex;

public class HelloworldEx {

	public static void main(String[] args) {
		
		for(int i = 0 ; i < 5 ; i++) {
			System.out.println("Hello World!");
		}
		
		System.out.println("=============");
		int i = 0;
		while(i < 5) {
			System.out.println("Hello World !");
			i++;
		}
	}

}
