package day3.ifex;

public class Ifex1 {

	public static void main(String[] args) {
		// 정수 num가 0이면 0입니다 라고 출력하는 코드를 작성하세요.

		int i = 0;

		if (i == 0) {
			System.out.println("0입니다.");
		}
		// 정수 num가 0이 아니면, "0이 아닙니다" 라고 출력하는 코드를 작성하세요.
		if (i != 0) {
			System.out.println("0이 아닙니다.");
		}
	}

}
